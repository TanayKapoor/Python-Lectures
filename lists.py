# Declaring list
simple_list = ["abc", "hghi", "lkmn", "def"]

# Sorting list
simple_list.sort()
print(simple_list)

# Insert in list
simple_list.insert(0, "xyz")
print(simple_list)

# Count
print(simple_list.count("xyz"))
simple_list.extend(["fgh", "ikj"])
print(simple_list)
simple_list.pop(2)
print(simple_list)
simple_list.reverse()
print(simple_list)
